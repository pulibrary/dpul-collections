defmodule DpulCollections.Searcher do
  @moduledoc "Search Princeton's digital library for interesting material by subjects and genres."

  use Hermes.Server.Component, type: :tool
  alias DpulCollections.Item
  alias DpulCollections.Solr
  alias Hermes.Server.Response

  schema do
    field :genres, {:list, {:enum, genres()}}
    field :subjects, {:list, {:enum, subjects()}}
  end

  @impl true
  def execute(_args = %{genres: genres, subjects: subjects}, frame) do
    search_state =
      DpulCollectionsWeb.SearchLive.SearchState.from_params(%{
        "per_page" => "10",
        "q" =>
          [suggest_query("subject_txtm", subjects), suggest_query("genre_txtm", genres)]
          |> Enum.filter(fn x -> x != nil end)
          |> Enum.join(" ")
      })

    solr_response = Solr.query(search_state)

    items =
      solr_response["docs"]
      |> Enum.map(&Item.from_solr(&1))

    response =
      Response.tool()
      |> Response.text(
        "The following are search results. There's a JSON response of each item followed by an embedded image thumbnail of that item. When presenting these items to users ensure that you provide the title, description (if present), a generated caption of the thumbnail (ensuring to clarify that you've generated the thumbnail caption and it wasn't provided), and the URL to the item. The item URL being at localhost is okay - present it anyways.",
        audience: [:assistant]
      )

    response =
      Enum.reduce(items, response, fn item, response ->
        response
        |> add_item(item)
      end)

    {:reply, response, frame}
  end

  def add_item(response, item) do
    response
    |> Response.json(%{
      title: item.title,
      description: item.description,
      url: "#{DpulCollectionsWeb.Endpoint.url()}/item/#{item.id}"
    })
    |> Response.embedded_resource(
      "#{item.primary_thumbnail_service_url}/square/100,100/0/default.jpg",
      format_item(item)
    )
  end

  def format_item(item) do
    [
      name: item.title,
      description: item.description,
      mime_type: "image/jpeg",
      blob: base64_image(item)
    ]
  end

  def base64_image(item) do
    thumbnail_image = "#{item.primary_thumbnail_service_url}/square/100,100/0/default.jpg"

    case Req.get(thumbnail_image) do
      {:ok, %{body: image_binary}} ->
        Base.encode64(image_binary)

      {:error, _reason} ->
        nil
    end
  end

  def suggest_query(_, []), do: nil

  def suggest_query(field, matches) do
    "#{field}:(#{matches |> Enum.join(" OR ")})"
  end

